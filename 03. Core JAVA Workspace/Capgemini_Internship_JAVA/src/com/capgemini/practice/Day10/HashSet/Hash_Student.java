package com.capgemini.practice.Day10.HashSet;

public class Hash_Student {

	private int sid;
	private String sname;

	public Hash_Student() {
		super();
	}

	public Hash_Student(int sid, String sname) {
		super();
		this.sid = sid;
		this.sname = sname;
	}

	public int getSid() {
		return sid;
	}

	public void setSid(int sid) {
		this.sid = sid;
	}

	public String getSname() {
		return sname;
	}

	public void setSname(String sname) {
		this.sname = sname;
	}

}