package com.cg.lab4_Inheritance_Polymorphism.Ex1_SavCurrAccount;

public class Person {
	private String name;
	private float age;

//    Getter
	public String getName() {
		return name;
	}

	public float getAge() {
		return age;
	}

//    Setter
	public void setName(String newName) {
		this.name = newName;
	}

	public void setAge(float newAge) {
		this.age = newAge;
	}
}